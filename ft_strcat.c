#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	size_t j;
	size_t i;

	j = 0;
	i = 0;
	while (s1[i])
		i++;
	while (s2[j] != '\0')
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
